UPDATE books b
SET categories = sub.cats
FROM (
  SELECT b2.id,
    ARRAY(
      SELECT DISTINCT COALESCE(m.canon, 'Otros')
      FROM unnest(b2.categories) AS raw(val)
      LEFT JOIN category_map m ON m.raw = raw.val
    ) AS cats
  FROM books b2
  WHERE b2.categories IS NOT NULL
    AND array_length(b2.categories, 1) > 0
) sub
WHERE b.id = sub.id;

